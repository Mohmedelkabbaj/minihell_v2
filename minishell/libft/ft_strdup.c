#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	int		i;

	i = 0;
	p = 0;
	p = malloc(ft_strlen((char *)s1) + 1);
	if (!p)
	{
		return (0);
	}
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
